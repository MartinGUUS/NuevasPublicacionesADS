    public void limpiarCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
    }

    public void guardarTXT() {
        String[][] dataTabla1 = new String[jTable1.getRowCount()][jTable1.getColumnCount()];
        String[][] dataTabla2 = new String[jTable2.getRowCount()][jTable2.getColumnCount()];

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            for (int j = 0; j < jTable1.getColumnCount(); j++) {
                dataTabla1[i][j] = jTable1.getValueAt(i, j).toString();
            }
        }
        
        for (int i = 0; i < jTable2.getRowCount(); i++) {
            for (int j = 0; j < jTable2.getColumnCount(); j++) {
                dataTabla2[i][j] = jTable2.getValueAt(i, j).toString();
            }
        }
        
        
        
        

    }

