    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1) { // Clic izquierdo
            int row = jTable2.getSelectedRow();
            int confirm = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas eliminar esta fila?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
                modelo.removeRow(row);
                jTable2.setModel(modelo);
            } else {
                int modifi = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas modificar esta fila?", "Modificar", JOptionPane.YES_NO_OPTION);
                if (modifi == JOptionPane.YES_OPTION) {
                    String codigo = (String) jTable2.getValueAt(row, 0);
                    String sku = (String) jTable2.getValueAt(row, 1);
                    jTextField1.setText(codigo);
                    jTextField2.setText(sku);
                    DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
                    modelo.removeRow(row);
                    jTable2.setModel(modelo);
                }

            }
        }

    }//GEN-LAST:event_jTable2MouseClicked

